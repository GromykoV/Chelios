void ABaseCharacter::SetWeaponCollisionEnabled(ECollisionEnabled::Type CollisionEnabled)
{
	if (Combat->EquippedEdgedWeapon && Combat->EquippedEdgedWeapon->GetWeaponBox())
	{
		Combat->EquippedEdgedWeapon->GetWeaponBox()->SetCollisionEnabled(CollisionEnabled);
		Combat->EquippedEdgedWeapon->IgnoreActors.Empty();
	}
}
